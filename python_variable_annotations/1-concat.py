#!/usr/bin/env python3
"""
    Function that concat two strings str1 and str2 and return a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    Function

    Args:
        str1 (str): string 1
        str2 (str): string 2

    Return:
        str: str1 + str2
    """
    return str1 + str2
